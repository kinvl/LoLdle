import Foundation

struct Spacing {
    struct Horizontal {
        /// Equal to 20.
        static let `default` = 20.0
        
        /// Equal to 30.
        static let mainButton = 30.0
        
        /// Equal to 40.
        static let big = 40.0
    }
    
    struct Vertical {
        /// Equal to 50.
        static let big = 50.0
        
        /// Equal to 30.
        static let `default` = 30.0
        
        /// Equal to 20.
        static let medium = 20.0
        
        /// Equal to 10.
        static let small = 10.0
        
        /// Equal to 5.
        static let tiny = 5.0
    }
}
